// crvserver_appflowpolicy_bindingSetAttrFromGet is used by the resource Read/Create
// read-back. The NITRO server may not echo back several user-supplied inputs (priority,
// gotopriorityexpression, bindpoint, invoke, labelname, labeltype, targetvserver). To
// avoid "inconsistent result after apply" (Pattern 7/13), only adopt the GET value when
// it is present; otherwise preserve the existing plan/state value already in `data`. The
// composite ID is set once in Create, so it is NOT recomputed here.
func crvserver_appflowpolicy_bindingSetAttrFromGet(ctx context.Context, data *CrvserverAppflowpolicyBindingResourceModel, getResponseData map[string]interface{}) *CrvserverAppflowpolicyBindingResourceModel {
	tflog.Debug(ctx, "In crvserver_appflowpolicy_bindingSetAttrFromGet Function")

	// name and policyname are the identity keys - always adopt from the GET response.
	if val, ok := getResponseData["name"]; ok && val != nil {
		data.Name = types.StringValue(val.(string))
	}
	if val, ok := getResponseData["policyname"]; ok && val != nil {
		data.Policyname = types.StringValue(val.(string))
	}

	// The remaining attributes are Optional+Computed. The NITRO GET echoes the ones the
	// user configured (priority/gotopriorityexpression/invoke/labelname/labeltype) but
	// omits unset ones (bindpoint/targetvserver). For an absent field we set Null (a known
	// value) so Terraform never sees "still unknown after apply" (Pattern 13). The server
	// echoes configured values verbatim, so adopting them causes no inconsistent-result diff.
	if val, ok := getResponseData["bindpoint"]; ok && val != nil {
		data.Bindpoint = types.StringValue(val.(string))
	} else {
		data.Bindpoint = types.StringNull()
	}
	if val, ok := getResponseData["gotopriorityexpression"]; ok && val != nil {
		data.Gotopriorityexpression = types.StringValue(val.(string))
	} else {
		data.Gotopriorityexpression = types.StringNull()
	}
	if val, ok := getResponseData["invoke"]; ok && val != nil {
		data.Invoke = types.BoolValue(val.(bool))
	} else {
		data.Invoke = types.BoolNull()
	}
	if val, ok := getResponseData["labelname"]; ok && val != nil {
		data.Labelname = types.StringValue(val.(string))
	} else {
		data.Labelname = types.StringNull()
	}
	if val, ok := getResponseData["labeltype"]; ok && val != nil {
		data.Labeltype = types.StringValue(val.(string))
	} else {
		data.Labeltype = types.StringNull()
	}
	if val, ok := getResponseData["priority"]; ok && val != nil {
		if intVal, err := utils.ConvertToInt64(val); err == nil {
			data.Priority = types.Int64Value(intVal)
		} else {
			data.Priority = types.Int64Null()
		}
	} else {
		data.Priority = types.Int64Null()
	}
	if val, ok := getResponseData["targetvserver"]; ok && val != nil {
		data.Targetvserver = types.StringValue(val.(string))
	} else {
		data.Targetvserver = types.StringNull()
	}

	// Re-derive the canonical id so a legacy SDK v2 id is upgraded to the new format on Read.
	idParts := []string{}
	idParts = append(idParts, fmt.Sprintf("name:%s", utils.UrlEncode(fmt.Sprintf("%v", data.Name.ValueString()))))
	idParts = append(idParts, fmt.Sprintf("policyname:%s", utils.UrlEncode(fmt.Sprintf("%v", data.Policyname.ValueString()))))
	data.Id = types.StringValue(strings.Join(idParts, ","))

	return data
}

