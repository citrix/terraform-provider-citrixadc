func TestAccPinger_basic(t *testing.T) {
	resource.Test(t, resource.TestCase{
		PreCheck:          func() { testAccPreCheck(t) },
		ProviderFactories: testAccProviderFactories,
		Steps: []resource.TestStep{
			{
				Config: testAccPing_basic,
				Check: resource.ComposeTestCheckFunc(
					testAccCheckPingExist("citrixadc_pinger.tf_pinger", nil),
				),
			},
		},
	})
}

func testAccCheckPingExist(n string, id *string) resource.TestCheckFunc {
	return func(s *terraform.State) error {
		rs, ok := s.RootModule().Resources[n]
		if !ok {
			return fmt.Errorf("Not found: %s", n)
		}

		if rs.Primary.ID == "" {
			return fmt.Errorf("No lb vserver name is set")
		}

		if id != nil {
			if *id != "" && *id != rs.Primary.ID {
				return fmt.Errorf("Resource ID has changed!")
			}

			*id = rs.Primary.ID
		}

		return nil
	}
}

const testAccPing_basic = `
resource "citrixadc_pinger" "tf_pinger" {
    hostname = "localhost"
}
`
